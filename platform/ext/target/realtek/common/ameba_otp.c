/**
 * \brief                               Reads the given OTP element.
 *
 * \param[in]  id                       ID of the element to read.
 * \param[in]  out_len                  Size of the buffer to read the element
 *                                      into in bytes.
 * \param[out] out                      Buffer to read the element into.
 *
 * \note                                If the size of the internal OTP
 *                                      representation of the item is different
 *                                      to out_len, then the smaller of the two
 *                                      is the amount of bytes that must be
 *                                      read.
 *
 * \retval TFM_PLAT_ERR_SUCCESS         The element is read successfully.
 * \retval TFM_PLAT_ERR_UNSUPPORTED     The given element has not been
 *                                      instanciated in OTP memory by this
 *                                      particular platform.
 * \retval TFM_PLAT_ERR_SYSTEM_ERR      An unspecified error occurred.
 */
enum tfm_plat_err_t tfm_plat_otp_read(enum tfm_otp_element_id_t id,
									  size_t out_len, uint8_t *out)
{
#ifdef MCUBOOT_BUILTIN_KEY
	/*NOTE: The key_ns/key_s are generated by CONFIG_TFM_KEY_FILE_NS/CONFIG_TFM_KEY_FILE_S with steps below:
	 *      1. openssl ec -in key.pem  -pubout -out key-pub.pem
	 *      2. openssl ec -in key-pub.pem -pubin -outform DER | tail -c 65 > key-pub.bin
	 *      3. xxd -i key-pub.bin
	 *      4. align to 4 bytes by padding 0x00
	 */
	extern unsigned char SECURE_KEY_PUB_ARRAY_NAME[];
	extern unsigned char NONSECURE_KEY_PUB_ARRAY_NAME[];
	extern unsigned int SECURE_KEY_PUB_ARRAY_LEN;
	extern unsigned int NONSECURE_KEY_PUB_ARRAY_LEN;
#endif

	if (id >= PLAT_OTP_ID_MAX) {
		return TFM_PLAT_ERR_INVALID_INPUT;
	}

	switch (id) {
	case PLAT_OTP_ID_HUK:
		/*TODO: */
		memset(out, 0, out_len);
		break;
	case PLAT_OTP_ID_IAK:
		/*TODO: */
		memset(out, 0, out_len);
		break;
	case PLAT_OTP_ID_ENTROPY_SEED:
		/*TODO: */
		memset(out, 0, out_len);
		break;
#ifdef MCUBOOT_BUILTIN_KEY
	case PLAT_OTP_ID_BL2_ROTPK_0:
		/* For secure image, corresponding to CONFIG_TFM_KEY_FILE_S*/
		memcpy(out, SECURE_KEY_PUB_ARRAY_NAME, MIN(out_len, SECURE_KEY_PUB_ARRAY_LEN));
		if (SECURE_KEY_PUB_ARRAY_LEN < out_len) {
			memset(out + SECURE_KEY_PUB_ARRAY_LEN, 0, out_len - SECURE_KEY_PUB_ARRAY_LEN);
		}
		break;
	case PLAT_OTP_ID_BL2_ROTPK_1:
		/* For non-secure image, corresponding to CONFIG_TFM_KEY_FILE_NS*/
		memcpy(out, NONSECURE_KEY_PUB_ARRAY_NAME, MIN(out_len, NONSECURE_KEY_PUB_ARRAY_LEN));
		if (NONSECURE_KEY_PUB_ARRAY_LEN < out_len) {
			memset(out + NONSECURE_KEY_PUB_ARRAY_LEN, 0, out_len - NONSECURE_KEY_PUB_ARRAY_LEN);
		}
		break;
#endif
	case PLAT_OTP_ID_LCS: {
		/*TODO: */
		enum plat_otp_lcs_t lcs = PLAT_OTP_LCS_SECURED;
		memcpy(out, (uint8_t *)&lcs, out_len);
		break;
	}
	default:
		DiagPrintf("tfm_plat_otp_read: unsupport id: %d, len: %u\n", id, out_len);
		return TFM_PLAT_ERR_UNSUPPORTED;
		break;
	}

	return TFM_PLAT_ERR_SUCCESS;
}

